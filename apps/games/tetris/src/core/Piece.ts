/** 获取踢墙偏移 */
export function getKicks(type: PieceType, from: Rotation, to: Rotation): [number, number][] {
  if (type === 'O') return [[0, 0]];
  const table = type === 'I' ? KICK_I : KICK_JLSTZ;
  return table[`${from}>${to}`] ?? [[0, 0]];
}

/** 获取方块形状矩阵 */
export function getShape(type: PieceType, rotation: Rotation): number[][] {
  return SHAPES[type][rotation];
}

/** 获取下一个旋转状态 */
export function nextRotation(rot: Rotation, dir: 1 | -1): Rotation {
  return ((rot + dir + 4) % 4) as Rotation;
}
